# dict.py - example of a dictionary / dict
# feature: a dict has key-valuepairs
song = {
    'artist': 'Madonna',
    'title': 'Like a virgin',
    'length': '4:44',
    'charts': 1
}
print('song artist:', song['artist'])  # Madonna
print('song title:', song['title'])  # Like a virgin

